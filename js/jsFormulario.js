function inc (){
    document.getElementById('inc').style.display ="block";
    document.getElementById('cacc').style.display ="none";
    document.getElementById('ppp').style.display ="none";
}
function cacc (){
    document.getElementById('inc').style.display ="none";
    document.getElementById('cacc').style.display ="block";
    document.getElementById('ppp').style.display ="none";
}
function ppp(){
    document.getElementById('inc').style.display ="none";
    document.getElementById('cacc').style.display ="none";
    document.getElementById('ppp').style.display ="block";
}